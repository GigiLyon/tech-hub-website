							<h2>Alibaba</h2>
							<p style="color:#808080; font-size:19px; font-weight:bold;">Alibaba unveils Hanguang 800 AI inference chip to speed-up ML tasks</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>Alibaba Group has introduced its first AI inference chip called ‘Hanguang 800’ which performs machine-learning tasks efficiently and quickly.

The neural processing unit is already being used to power features on Alibaba’s e-commerce sites, including product search and personalised recommendations. The Hanguang 800 will be made available to Alibaba Cloud customers at a later stage.

According to Alibaba, its ecommerce website Taobao previously took an hour to categorise one billion product images that are uploaded to the site each day by merchants and prepare them for search and personalised recommendations. However, with the Hanguang 800, Taobao was able to finish the task in just five minutes.  </p>

							<p>Apart from using the Hanguang 800 in many business operations, the chip is also used for automatic translation on its e-commerce sites, advertising, and intelligent customer services.</p>

							<p>Jeff Zhang, Alibaba Group CTO and president of Alibaba Cloud Intelligence, said:

“The launch of Hanguang 800 is an important step in our pursuit of next-generation technologies, boosting computing capabilities that will drive both our current and emerging businesses while improving energy-efficiency. In the near future, we plan to empower our clients by providing access through our cloud business to the advanced computing that is made possible by the chip, anytime and anywhere.”</p>

							<p>The Hanguang 800 was created by T-Head, the unit that leads the development of chips for cloud and edge computing within Alibaba DAMO Academy. The unit has developed the chip’s hardware and algorithms designed for business apps, including Alibaba’s retail and logistics apps.

It is believed that the Hanguang 800 would help Chinese companies to reduce their dependence on American technology, as the trade war makes business collaborations between the two countries more and more difficult.

The company has not revealed any details on the availability of the chip so far.</p>
								
