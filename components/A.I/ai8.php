							<h2>Home Security Systems</h2>
							<p style="color:#808080; font-size:19px; font-weight:bold;">Report: AI may increase sales of home security systems</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>When home security systems were first introduced, they seemed to be pretty much revolutionary in terms of technology. However, today hundreds of companies are offering their own version of DIY security systems that can easily be bought from chain hardware stores. Thanks to millions of American consumers for adopting the technology who are responsible for the expansion of the residential security segment in the country. The growth in this vertical seems so positive that Parks Associates is claiming the market to expand by the end of 2019.
Based on its study, Parks Associates found 23% of the self-monitored systems are fee-based with an average cost of $9 (£6.99) per month, while 22% also include home control. By 2024, the average monthly fees will slide down to $8.50 (£6.6) whereas 34% will consist of home control add-on services. In order to cash-in on home security services, the market researcher believes that the industry will have to find new drivers for the homes that currently do not offer such innovative services.</p>

							<p>Parks Associates’ senior analyst Dina Abdelrazik opines: “While smart home features may tip the scales toward acquisition, overall market drivers and inhibitors for home security are roughly the same as they have been for many years. Parks Associates forecasts roughly 30% of US broadband households will have a security system by 2024, so the industry is in need of business models that can tap into the roughly 70% of households without a security system.”

Parks strongly believes in the potential of artificial intelligence (AI) and says that the technology can play a crucial role in boosting innovation in the home security and monitoring market, attracting a broader base of customers. In addition, AI combined with machine learning could reduce chances of false alarms, enhance detection of anomalous activities and performance of video analytics and offer better video verification and surveillance capabilities. Introduction of this combination in security and monitoring systems can attract consumers at large.</p>

						<p>Abdelrazik says: “A complete value proposition based on AI benefits would require a full alignment of AI-enabled hardware, system software, and intelligent call center services. Vertically integrated or partially vertically aligned players are in the strongest position to deliver these benefits because they can optimize the whole system, assure quality control and user experience, and monetize each component.”

According to the market researcher, there is a huge opportunity for subscribers to buy technology products from service providers as nearly 86% of American households have fixed broadband access today. Services providers are also at an advantage of being the broadband providers into these homes that are considering home security or monitoring systems – this means those customers could get those services on their existing cable or telco bills.</p>

