							<h2>Huawei</h2>
							<p style="color:#808080; font-size:19px; font-weight:bold;">Huawei announces its own AI assistant as it prepares for Google-less life</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>Huawei has announced its own AI-powered assistant during a launch event in Munich as it prepares for life without Google’s services.

Due to US trade restrictions, Huawei is losing access to Google’s services. The new Mate 30 smartphones announced in Munich today will launch with the open-source Android, but it will not feature the Play Store, Gmail, YouTube, Google Pay, or the many other services which Western consumers are used to.

Among the features that will be missing from the Mate 30 onwards is Google Assistant. Huawei is quickly working to fill the gaps left without access to Google’s services with its own and is launching the Huawei Assistant as a replacement for Mountain View’s virtual assistant.</p>

							<p>Walter Ji, Director of Business, HUAWEI Consumer Business Group Western Europe, said:

“With our focus on user experience, we bring AI into mobile services so we can proactively identify user needs and thus improve their smartphone experience.

Huawei Assistant is a product that intelligently fulfils user needs at the same time as offering partners an opportunity to provide their services to users through a globally-available distribution platform.”

Huawei Assistant will launch with basic functionality compared to Google’s version, but the company is promising to expand it.

By swiping to the right of the homescreen, much like accessing Google Assistant today, users can begin interacting with Huawei Assistant. The service is powered by Huawei Ability Gallery, a service distribution platform.</p>

						

							<h3>There are four key features of the Huawei Assistant:</h3>

							<p><b>1. Newsfeed</b> – Today’s Google Assistant provides some personalised articles when you swipe to it on an Android device. The newsfeed feature is Huawei Assistant’s alternative but users can decide whether to receive custom recommendations or to select from news agencies to fill their feed with “up-to-the-minute” articles.</p>
							<p><b>2. Search</b> – Users can search for information on their smartphone using Huawei Assistant. The assistant will surface things such as installed apps, memos, emails, and calendar entries, while also providing an online search feature using the default browser.</p>
							<p><b>3. Instant Access</b> – Four shortcuts to a user’s choice of applications can be selected for quick access. In the future, Huawei says this can make use of AI so the shortcuts are intelligently-selected based on what the user may want at that moment.</p>
							<p><b>4. SmarterCare</b> – Real-time information will be provided using AI. At launch, this will mean things such as the weather forecast, missed calls, and schedule reminders. Future planned functionality will enable more powerful abilities like booking restaurants, flights, taxis, and hotels.</p>

							<p>The new assistant from Huawei will be pre-installed on Mate 30 series devices but it will also be downloadable from the company’s App Gallery.</p>
