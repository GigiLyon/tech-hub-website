						<header class="special">
							<h2>BlackBerry refreshes its UEM suite, focuses on zero-trust access</h2>
							<p style="color:#808080; font-weight:bold; font-size:19px;">BlackBerry's latest version of its flagship Enterprise Mobility Suite is aimed at enabling management of all devices and apps across all vendor platforms.</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>BlackBerry has unveiled several updates to its enterprise mobility security platform, offering three new 
								UEM peoducts aimed at enabling secure access to tools, applications and files based on a zero-trust
								architecture. The trio of new suites are a strategic update to BlackBerry's flagship Enterprise 
								Mobility Suite. Zero trust relies on multifactor authentication, analytics, encryption and file 
								system-level permissions; it includes dynamic enforcement of access rules, not only for a user's 
								identity but also for their device and the context in which they're attempting access. The result 
								is that users are given the minimum amount of access to accomplish a specific task. The new BlackBerry
								Secure UEM & Productivity Suites come in three versions.</p>

							<p>The Choice Suite allows IT admins to manage all devices and apps, across platforms and device ownership 
							   models. The single platform streamlines device management and simplifies IT and end user experiences, 
							   according to BlackBerry. The Freedom Suite allows users to improve mobility with collaboration applications, 
							   app customization and enterprise-grade security. "Building on capabilities included in the Choice Suite, 
							   the Freedom Suite offers Mobile Content Management (MCM), Identity and Access Management (IAM), and 
							   the ability for users to access files in Microsoft mobility apps," Blackberry said in a statement. It also
							   includes development tools to add security, mobility and platform features to custom apps, regardless of 
							   how developers build them. The Limitless Suite offers Digital Rights Management (DRM) for file protection, 
							   expanding on on the Choice and Freedom Suites with multi-channel notification capabilities and a secure 
							   content collaboration platform.</p>

							<p>BlackBerry provides multi-OS management, so the UEM software can be used to manage different devices and 
							   platforms, including iOS, Android, Windows 10, macOS and Chrome OS."BlackBerry phones would be just one 
							   type of device we can manage, but it's much broader," a company spokesperson said via email. “Our UEM 
							   offerings are designed to protect all devices and endpoints in the enterprise." While research firm 
							   Gartner sees BlackBerry in the company of other UEM vendors such as Microsoft, VMware and MobileIron, 
							   it is no longer a competitive player in the handset market, according to Jack Gold, principal analyst 
							   with J. Gold Associates. "Their entire effort in the past couple of years has been around mobile software, 
							   specifically around UEM and the QNX OS primarily targeted at automotive (and a few other [software] assets 
							   they have)," Gold said via email. "As a standalone UEM [vendor], BlackBerry is comparable to many others 
							   in the market."</p>

							<p>Where BlackBerry has moved ahead of other UEM rivals is in security, having bought Cylance – an AI security 
								offering – earlier this year. "They are now porting those AI security features to the mobile device world 
								from their previously PC-centric offering," Gold said. "They are also moving in a workspaces direction as 
								they offer secured on-device access through secured browsers and [Office] 365 type access." Enterprise IT 
								and operations leaders responsible for mobile and endpoint strategy must evaluate new capabilities along 
								with UEM tools' ability to perform modern management of PCs and management of mobile devices, according to 
								Gartner's 2019 UEM Magic Quadrant report. The adoption of Windows 10, Google Chrome OS and Apple's macOS 
								will drive the need for a combined endpoint management console in greater than 70% of organizations by 2024, 
								Gartner said. </p>
								
							<p>BlackBerry Enterprise BRIDGE and Microsoft Graph API support provides flexibility to Office 365 
								organizations in how those apps are managed and integrated into business workflows, according to Gartner. 
								Although Citrix and VMware are moving much more aggressively toward a modern-workspaces approach that is more 
								than just UEM, and exceed them in a security-enabled product, BlackBerry does have an attractive offering, 
								"particularly for high security industries," such as government, financial services and healthcare and 
								"they compete very well there with Mobile Iron, Citrix, VMware," and others in the UEM space, Gold said.
							</p>

					

