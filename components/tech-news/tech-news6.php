						<header class="special">
							<h2>Salesforce to expand Einstein Voice Skills so users can create custom voice apps</h2>
							<p style="color:#808080; font-weight:bold; font-size:19px;">Salesforce is among a number of tech firms aiming to bring voice to the enterprise, 
							though most users will have to wait until 2021 to access its AI voice assistant.</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>Salesforce plans to expand its Einstein Voice Assistant capabilities, enabling customers to create their own 
								custom “skills” as it seeks to broaden the appeal of voice interfaces in the workplace. The company unveiled 
								Einstein at last year’s Dreamforce conference, promising to enable sales staff to update customer records 
								on its CRM platform using voice commands. Its efforts are part of a wider push from numerous tech firms to 
								popularize conversational voice interfaces for business apps, including Amazon’s Alexa for Business, 
								Microsoft’s Cortana and Oracle’s Digital Assistant. Analyst firm Gartner forecast that 25% of digital 
								workers will interact with virtual assistants on a daily basis within two years. On Tuesday at this year’s 
								Dreamforce, Salesforce unveiled a handful of voice-related updates. With Einstein Voice Skills, developers 
								can create voice-powered apps for Salesforce’s Customer 360 Platform, expanding the range of potential uses, 
								the company said.</p> 
								
							<p>The skills could, for instance, allow field technicians to check on a service history while driving to their 
								next customer, the company said. “Voice is a huge shift for the industry and will be as impactful in businesses 
								as it’s been in our homes,” said Bret Taylor, president and chief product officer at Salesforce, in a statement. 
								“With Einstein, Salesforce is bringing the power of voice to every business, giving everyone an intelligent, 
								trusted guide at work.” While AI-based voice technology has potential, it is still nascent in terms of its use 
								in enterprise applications, said Nicholas McQuire, vice president for enterprise research at CCS Insight. 
								“Obviously, voice is becoming a trend in mobile as the next phase of the phone and application interface. 
								But it is still going to take some time for business professionals to take hold of this technology, and for 
								voice to be the dominant modality of interaction with applications,” said McQuire.</p>

							<p>Salesforce is not in a rush to bring its voice assistant, which has been in a private pilot since the summer, 
								to market. Although Einstein Voice Assistant and Einstein Voice Skills will be available in beta in February, 
								a full rollout is not planned until 2021. McQuire said that the biggest opportunity for Salesforce with 
								conversational interfaces, in the short term, lies in customer-facing voice interactions. That’s an area 
								the company focused on with the announcement of Einstein Voice Bots at last year’s Dreamforce event. 
								Einstein Voice Bots will enable developers to build corporate branded bots that link to Salesforce CRM 
								and can be accessed via Google Assistant and Amazon Alexa. “When it comes to the customer and consumer 
								applications, it is a different ball game,” said McQuire. "From a customer perspective, there is an increasing 
								drive to interact with brands and with services using different modalities. ...Voice is one that we are starting 
								to see more and more.</p>

							<p>“Salesforce will probably see the maturity curve of voice starting with the customer first, and over the next 
								three to five years we will see it creep in more and more to the enterprise," he said. At Dreamforce, 
								Salesforce also unveiled the Einstein Call Coaching feature for its Sales Cloud. It lets managers quickly 
								see key phrases in sales call transcripts, highlighting trends in conversation data such as an increase in 
								mentions of a rival company. It can also identify best practices around objection handling and pricing 
								discussions that can be used to give sales reps personalized training. That feature is currently being 
								piloted and will be available in June 2020. Salesforce also announced that it has integrated telephony into 
								its Service Cloud console with the introduction of Service Cloud Voice. It’s designed to offer users access 
								to AI capabilities such as real-time transcriptions with AI-generated suggested actions that help call center 
								staffers resolve issues faster.</p> 

