						<header class="special">
							<h2>Microsoft goes very small for Windows 10 1909's 'On' switch</h2>
							<p style="color:#808080; font-weight:bold; font-size:19px;">The latest version of Windows relies on an 'enablement package' to turn on features that transform Windows 10 1903 into 1909. Microsoft now calls that move a pilot program.</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>Microsoft has trumpeted the smaller size of Windows 10 1909, the fall feature upgrade that 
							 broke the twice-annual mold the company used in 2017 and 2018. But how much smaller is it?
							 The so-called "enablement package," the download that when installed transforms Windows 10 1903 - 
							 the full-featured upgrade released May 21 - into November's 1909, weighs in at just 180KB, as in kilobytes.
							 (For context, 180KB is about 0.18MB, or less than 20% of a megabyte. More telling, it's about 10 times the
							  size of a two-page Microsoft Word document. Downloading a 180KB file would be seemingly instantaneous 
							  on all but the slowest broadband connection; a torpid 1Mbps connection, for instance, would download that 
							  file in about one second.) "The 180KB package is just the thing that turns on the light switch," said one 
							  of the team members, who had gathered around a conference table.</p>

							<p>Indeed. As Computerworld has detailed elsewhere, Windows 10 1909 is, unlike previous feature upgrades,
							   nearly feature-free. Instead, it's a Windows 10 analog to the old-school service packs of the past, 
							   catch-up collections of fixes made to a current but aging OS from the time it debuted (or was issued 
							   an earlier service pack). In 1909's case, it was months', not years' worth, of fixes, because Microsoft 
							   took May's 1903, collected everything that changed from that point on, then added a handful of new features.
								Voil√†...Windows 10 1909. The two - 1903 and its rehash successor 1909 - are, Microsoft said, identical 
								with the exception of those few new features baked into the latter. "Windows 10, versions 1903 and 1909
								share a common core operating system and an identical set of system files," is how Microsoft put it. 
								The cumulative update for 1903 released Nov. 12, in fact, was identical to the delivered-same-day Windows
								10 1909. </p>

							<h2>1909 is a pilot?</h2>

							<p>Along with the revelation that the transformative enablement package was a puny download, the Insider team 
								also commented on one of the most-important, yet-unanswered questions about 1909. Is this un-upgrade concept,
								with a feature-packed refresh released in the spring and a rehash issued in the fall, how Microsoft will
								carry on going forward? "Will we see this cycle for every year?" a question asked in one slide marked FAQ
								that was shown during the Thursday webinar. "Major feature update in H1, more minor update in H2, one 
								cumulative update for both?" The reply was more illuminating than an earlier-this-week decline-to-comment 
								response from a Microsoft spokeswoman. "Delivering the 19H2 feature update via cumulative update and an 
								enablement package is a pilot program," the FAQ stated. "There isn't a formal plan in place to deliver 
								future releases in the same way."</p>

							<p>This was the first time Microsoft has referred to the major-minor cadence exhibited by 1903 and 1909 as a 
								"pilot program." But the latest phrasing still denies that a decision has been reached, that, in fact, 
								there is remains a possibility Windows 10's servicing model might return to a tempo of two equally full 
								upgrades each year. ( Computerworld has argued, first, that returning to the major-major model would 
								eliminate benefits now offered to enterprise customers and two, repudiate the time and money spent on 
								coming up with what seemed, to outsiders at least, a complicated solution for Windows 10 1809's debacle.)
								"We are closely monitoring feedback and hoping to learn from this type of release to help influence our 
								future plans," Microsoft concluded in the FAQ. "A lot of this was to see ... to figure out if we could 
								do it," said another team member about the 1909 un-upgrade and whether it was a one-off.</p>
								
							<p>	"Are we doing this again? There is no formal plan saying that there's going to be a 20H2 the same way a 
								19H2 was done. We wanted to try this and see how it turned out. Obviously, there was a ton of things we 
								learned along the way: The downstream impacts, the domino effects of how this all fits together. But as 
								we continue to work through this with Insider preview builds, we'll see what happens."</p>

