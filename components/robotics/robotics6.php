							<h2>AMAZON ROLLS OUT NEW DELIVERY ROBOT CALLED SCOUT</h2>
							<p style="color:#808080; font-size:19px; font-weight:bold;">An Amazon employee will accomany the autonomous machines at first to ensure they can safely navigate around pedestrians and pets</p>
