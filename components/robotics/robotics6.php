						<div class="content" style="text-align:justify">
							<p>Amazon has launched a new robot delivery service in the US using a six wheeled machine that is "the size of a small cooler".

The Amazon Scout uses self-driving technology to navigate through neighbourhoods to deliver packages to Amazon Prime customers, though its initial roll out is limited.

The first deliveries are taking place in Snohomish County, just to the north of Amazon's headquarters in Seattle, Washington.</p>

							<p>Deliveries will be limited to daylight hours between Monday and Friday amd only six of the Scout delivery robots will be deployed at first.

Each will also be accompanied by an Amazon employee to ensure they can safely navigate around any pedestrians or pets it might come across. Sean Scott, vice president of Amazon Scout, said the Scout robot will be part of a growing number of delivery solutions for the online retail giant.</p>

							<p>"We are happy to welcome Amazon Scout to our growing suite of innovative delivery solutions for customers and look forward to taking the learnings from this first neighborhood so Amazon Scout can, over time, provide even more sustainability and convenience to customer deliveries," Mr Scott said.

Snohomish County executive Dave Somers added: “We are delighted to welcome Amazon Scout into our community. Similar to Amazon, we are always looking for new ways to better deliver service to our residents.”</p>

							<p>Other delivery methods Amazon is currently working on include autonomous drones, which are already being tested in the UK. 

It is hoped such technologies will dramatically reduce delivery times, with the first Prime Air delivery in 2016 dropping off a package within 13 minutes of being ordered.
"It looks like science fiction, but it's real," Amazon said at the time. "One day, seeing Prime Air vehicles will be as normal as seeing mail trucks on the road."

Amazon is yet to reveal if and when the Scout robot or Prime Air will be introduced to customers on a wider scale.
</p>
								
