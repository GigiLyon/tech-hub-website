						<div class="content" style="text-align:justify">
							<p>Uber has finally confirmed it is testing self-driving cars in the US.

The taxi company has long been rumoured to be developing the technology, but it has finally come clean in a blog post.

According to Uber, autonomous cars developed at the company's Advanced Technologies Centre have begun road tests in Pittsburgh, Pennsylvania, with more set to come in the next few weeks.</p>


							<p>The hybrid Ford Fusion cars will spend their time collecting road data and testing out their self-driving capabilities. Fortunately for other road users, a human driver will be behind the wheel at all times, even if they won't actually be driving the car.

One of the vehicles was tested by the Pittsburgh Tribune, who said it managed to succesfully make turns, navigate windy roads, and stop at red lights all by itself.</p>

<p>Uber says its goal with the technology is to cut the number of deaths caused by road accidents, around 94 per cent of which involve human error. Self-driving cars have another big advantage for the company, however - unlike human drivers, they don't need to be paid.</p>

							<p>The road tests come with the blessing of Pittsburgh's Mayor, William Peduto, who said: "From the first steel mills to the laboratories at Pitt and Carnegie Mellon, Pittsburgh has a long history of innovation."</p>
								
							<p>"We're excited that Uber has chosen the Steel City as they explore new technologies that can improve people's lives - through increased road safety, less congestion, and more efficient and smarter cities."

The technology is very much in its infancy at the moment, so don't expect a driverless Uber to arrive outside your house any time soon.</p>

