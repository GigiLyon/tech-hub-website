							<p>Chinese robotics researchers have created a humanoid robot named Jia Jia, who can move her arms, make different facial expressions, and respond to human conversation.

Jia Jia, the product of three years' work by a team at the University of Science and Technology of China in Hefei, greeted the assembled audience at her unveiling by saying: "Hello everyone, I'm Jia Jia. Welcome!"</p>

							<p>Jia Jia's limited motion and stilted speech shouldn't leave anyone in any doubt she's a robot. However, she looks fairly realistic, with a flexible plastic face, long flowing brown hair, and an eye-catching gold dress.
                            According to the team's director, Chen Xiaoping, particular attention was paid to her eye movements and lip synchronisation. He told reporters at the unveiling that the team's next step is to make it possible for her to laugh and cry.
                            Even without this full spectrum of emotions, she's still much less unsettling than the robotic Scarlett Johansson, created earlier this year by Hong Kong robot builder Ricky Ma.</p>

							<p>Jia Jia was shown speaking with her creators, but her repertoire of lines might need a little work - after being switched on, she greeted Chen as her "Lord", and told assembled photographers to step back when taking pictures, so her face wouldn't look too fat.

As XinhuaNet reports, the team's eventual aim is to give the robot 'learning' abilities and facial recognition, so its human interactions gradually become more natural.

The team are set to take Jia Jia to Shanghai's International Technology Fair on 21 April, where she will be shown off to investors and tech experts from around the world.</p>

<p>However, they currently have no plans to put what they call their "robot goddess" into mass production, so you may have to wait a while to get one in your own home.</p>
