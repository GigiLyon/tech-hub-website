						<div class="content" style="text-align:justify">
							<p>A robotic finger that plugs into your smartphone could let loved ones send a remote caress alongside an emoji or empower your device to crawl across your desk, a demonstration by French researchers shows.

Developer Marc Teyssier and colleagues from Paris-Saclay University and Sorbonne University created the MobiLimb prototype to explore how robotic prosthetics could give our inert devices new uses or personas.</p>

							<p>He likens this to the “biohacking” movement, where have-a-go scientists are implanting microchips beneath their skin to monitor their vital signs or allow contactless payments.  

Mr Teyssier writes on his website: “In the spirit of human augmentation, which aims at overcoming human body limitations by using robotic devices, our approach aims at overcoming mobile device limitations (static, passive, motionless) by using a robotic limb.”

A video demonstrating the motorised digit’s myriad uses show it acting as an automated stand for phones and tablets, a joystick for gaming, or a flexible light. </p>


							<p>There are also eerie shots of it sheathed in prosthetic human skin (or fur) to allow it to sensitively touch users and add a human dimension to instant messaging.

However NHS staff on social media were quick to warn the device may lead to phones being used for infinitely more intimate interactions. Radiographers warned it could mean even more demand for their specialist services.
Advances in robotics are making it possible for people who have lost limbs to take on everyday tasks which would be impossible with less advanced prosthetics, and can even pass on the sensations of touch.</p>
								
							<p>Among the biohacker movement, individuals are using these same advances for entirely new functions, such as vibrating implants or extra limbs.</p>

