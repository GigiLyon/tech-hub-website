						<div class="content" style="text-align:justify">
							<p>A blind woman fitted with a “bionic eye” has spoken of her joy after she was able to tell the time for the first time in more than five years.

Rhian Lewis, 49, was given the retinal implant as part of an ongoing trial at Oxford’s John Radcliffe Hospital.

The mother of two, from Cardiff, has suffered from retinitis pigmentosa – an inherited disorder – since she was five. The condition causes gradual deterioration of the light-detecting cells (photoreceptors) in the retina, which can lead to blindness.
One in 3,000 to 4,000 people in the UK have the disease, for which there is currently no cure.</p>

							<p>Ms Lewis is completely blind in her right eye and has virtually no vision in her left. The implant, made by German firm Retina Implant AG, was placed in Ms Lewis’s eye in June. During follow-up tests, Ms Lewis was asked to look closely at a large cardboard clock to see if she could tell the time correctly. She had not been able to do this with her right eye in 16 years and for about six years with her left eye.

When she realised she could tell that the time was three o’clock, she said “Oh my God! That felt like Christmas Day.” Describing the moment the device was turned on, Ms Lewis said: “They said I might not get any sensation and then all of a sudden within seconds there was this flashing in my eye, which has seen nothing for over 16 years, so it was like, oh wow!”

</p>


							<p>Ms Lewis was then taken to the cloisters of New College, Oxford, to see if she could make out its features. She said: “The lady from Social Services told me to point out anything I thought might be there. And there was a car, a silver car, and I couldn’t believe it, because the signal was really strong. I was just so excited.”</p>

