							<h2>Elon Musk explains why Tesla’s Cybertruck windows smashed during presentation</h2>
							<p style="color:#808080; font-size:19px; font-weight:bold;">Softening things up with a sledgehammer beforehand didn’t help</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>The unveiling of Tesla’s Cybertruck last week was full of surprises, but none more shocking than the moment when lead designer Franz 
                                von Holzhausen smashed two of the vehicle’s “armor glass” windows onstage with a metal ball. It certainly wasn’t the result CEO Elon Musk was expecting, who could be heard muttering “oh my fucking god” under his breath before being forced to complete his presentation in front of the fractured panes.</p>

							<p>But Musk says he knows what went wrong, and explained things on Twitter. Right before the metal ball test, von Holzhausen smacked the door with a sledgehammer on stage to prove its durability (and unlike the glass, it was fine), and Musk says this impact “cracked base of glass,” which is why the windows subsequently smashed when hit by the ball.
                            This seems plausible, especially as Musk also shared a slow motion video of von Holzhausen performing the same exact test before the event, with the ball bouncing harmlessly off the window. The combined impacts likely weakened the glass, setting the stage for the eventual smash. (Though why the back window broke as well isn’t clear: the passenger door didn’t get whomped by the sledgehammer.)
                            </p>

							<p>At any rate, the smashed glass was just one moment in an event which gave viewers plenty to talk about without the on-stage mishaps. The divisive design and impressive specs of the Cybertruck have caught the world’s attention, and since the unveiling Musk has been drip-feeding bits of information on Twitter to keep people engaged.

Solar panels on the roof that charge the car as it drives? We’re doing it, says Musk. A new matte black finish? “Sure,” he says. The Tesla CEO even suggested a second, “smaller” version of the Cybertruck would make sense in the “long term.”</p>

							<p>We’ll have to wait and see how many of these promises become a reality (remember: Musk has been predicting Teslas will have “full self driving” capability for years) but there’s certainly a lot of interest in the Cybertruck. As of Monday morning, Musk said the company had received more than 200,000 preorders for the vehicle, “with no advertising & no paid endorsement.” 
                                All it took was a few smashed windows instead.</p>
								
							<p>	"Are we doing this again? There is no formal plan saying that there's going to be a 20H2 the same way a 
								19H2 was done. We wanted to try this and see how it turned out. Obviously, there was a ton of things we 
								learned along the way: The downstream impacts, the domino effects of how this all fits together. But as 
								we continue to work through this with Insider preview builds, we'll see what happens."</p>

