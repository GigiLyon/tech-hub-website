							<h2>‘KILLER ROBOTS’ WILL START SLAUGHTERING PEOPLE IF THEY’RE NOT BANNED SOON, AI EXPERT WARNS</h2>
							<p style="color:#808080; font-size:19px; font-weight:bold;">'These will be weapons of mass destruction'</p>
