						<div class="content" style="text-align:justify">
							<p>An artificial intelligence expert has called for countries to ban so-called “killer robots” before activists’ warnings against them become a reality. 

The Campaign to Stop Killer Robots recently released a short film, in which autonomous weapons are used to carry out mass killings with frightening efficiency, while people struggle to work out how to combat them.

A United Nations panel discussed the issue last week, but next plans to meet next year.</p>

							<p>Toby Walsh, Scientia Professor of AI at UNSW Sydney, says he’s “confident” that killer robots will be banned, but is worried that the decision could take a long time to make. 

“[The] arms race has happened [and] is happening today,” he said at the UN, reports AFP. 

“These will be weapons of mass destruction. I am actually quite confident that we will ban these weapons … My only concern is whether [countries] have the courage of conviction to do it now, or whether we will have to wait for people to die first.”

However, Amandeep Gill, who chaired the Convention on Conventional Weapons meeting, has played down such fears.</p>

							<p>“Ladies and gentlemen, I have news for you: the robots are not taking over the world. Humans are still in charge,” he said. 
                            The panel has agreed to proceed with talks on defining and possibly setting limits on autonomous weapons, and is weighing up the potential creation of a code of conduct. 

However, Mr Gill revealed that the panel doesn’t plan to meet again until 2018, the Times of Israel reports.

In response to criticism about its speed of progress, he said, “I think we have to be careful in not emotionalising or dramatising this issue.”</p>

							<p>“[Artificial intelligence’s] potential to benefit humanity is enormous, even in defense,” said Stuart Russell, a professor of computer science at the University of Berkeley, who featured in the film released by the Campaign to Stop Killer Robots.

“But allowing machines to choose to kill humans will be devastating to our security and freedom. Thousands of my fellow researchers agree. We have an opportunity to prevent the future you just saw, but the window to act is closing fast.”

Earlier this month, hundreds of AI experts urged the Canadian and Australian governments to treat autonomous weapons in the same way as chemical biological and nuclear weapons, arguing that delegating life-or-death decisions to machines crosses a moral line, and must not be allowed to happen. </p>
								
