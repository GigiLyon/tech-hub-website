							<h2>What is Python? Powerful, intuitive programming</h2>
							<p style="color:#808080; font-weight:bold; font-size:19px;">Why the Python programming language shines for data science, machine learning, systems automation, web and API development, and more</p>
						</header>
						<div class="content" style="text-align:justify">
							<p>Dating from 1991, the Python programming language was considered a gap-filler, a way to write scripts that “automate the boring stuff” (as one popular book on learning Python put it) or to rapidly prototype applications that will be implemented in other languages.

However, over the past few years, Python has emerged as a first-class citizen in modern software development, infrastructure management, and data analysis. It is no longer a back-room utility language, but a major force in web application creation and systems management, and a key driver of
 the explosion in big data analytics and machine intelligence.</p>

 <h3>Python’s key advantages</h3>

							<p>Python’s success revolves around several advantages it provides for beginners and experts alike.</p>

							<h3>1. Python is easy to learn and use</h3>
							<p>The number of features in the language itself is modest, requiring relatively little investment of time or effort to produce your first programs. The Python syntax is designed to be readable and straightforward. This simplicity makes Python an ideal teaching language, and it lets newcomers pick it up quickly. As a result, developers spend more time thinking about the problem they’re trying to solve and less time thinking 
								about language complexities or deciphering code left by others.</p>
							
							<h3>2. Python is broadly adopted and supported</h3>
							<p>Python is both popular and widely used, as the high rankings in surveys like the Tiobe Index and the large number of GitHub projects using Python attest. Python runs on every major operating system and platform, and most minor ones too. Many major libraries and API-powered services have Python bindings or wrappers, letting Python interface freely with those services or directly use those libraries. </p>

							<h3>3. Python is not a “toy” language</h3>
							<p>Even though scripting and automation cover a large chunk of Python’s use cases (more on that later), Python is also used to build professional-quality software, both as standalone applications and as web services. Python may not be the fastest language, but what it lacks in speed, it makes up for in versatility.</p>

							<h3>4. Python keeps moving forward</h3>
							<p>Each revision of the Python language adds useful new features to keep pace with modern software development practices. Asynchronous operations and coroutines, for instance, are now standard parts of the language, making it easier to write Python apps that perform concurrent processing.</p>
							
							<h3>What Python is used for</h3>
							<p>The most basic use case for Python is as a scripting and automation language. Python isn’t just a replacement for shell scripts or batch files; it is also used to automate interactions with web browsers or application GUIs or to do system provisioning and configuration in tools such as Ansible and Salt. But scripting and automation represent only the tip of the iceberg with Python.</p>

							<h3>1. General application programming with Python</h3>
							<p>You can create both command-line and cross-platform GUI applications with Python and deploy them as self-contained executables. Python doesn’t have the native ability to generate a standalone binary from a script, but third-party packages like cx_Freeze and PyInstaller can be used to accomplish that.</p>
							
							<h3>2. Data science and machine learning with Python</h3>
							<p>Sophisticated data analysis has become one of fastest-moving areas of IT and one of Python’s star use cases. The vast majority of the libraries used for data science or machine learning have Python interfaces, making the language the most popular high-level command interface to for machine learning libraries and other numerical algorithms.</p>
							
							<h3>3. Web services and RESTful APIs in Python</h3>
							<p>Python’s native libraries and third-party web frameworks provide fast and convenient ways to create everything from simple REST APIs in a few lines of code to full-blown, data-driven sites. Python’s latest versions have strong support for asynchronous operations, letting sites handle tens of thousands of requests per second with the right libraries.</p>

							<h3>4. Metaprogramming and code generation in Python</h3>
							<p>In Python, everything in the language is an object, including Python modules and libraries themselves. This lets Python work as a highly efficient code generator, making it possible to write applications that manipulate their own functions and have the kind of extensibility that would be difficult or impossible to pull off in other languages. Python can also be used to drive code-generation systems, such as LLVM, to efficiently create code in other languages.</p>

							<h3>“Glue code” in Python</h3>
							<p>Python is often described as a “glue language,” meaning it can let disparate code (typically libraries with C language interfaces) interoperate. Its use in data science and machine learning is in this vein, but that’s just one incarnation of the general idea. If you have applications or program domains that you would like to hitch up, but cannot talk to each other directly, you can use Python to connect them.</p>
							
							<h3>Where Python falls short</h3>
							<p>Also worth noting are the sorts of tasks Python is not well-suited for.

Python is a high-level language, so it’s not suitable for system-level programming—device drivers or OS kernels are out of the picture.

It’s also not ideal for situations that call for cross-platform standalone binaries. You could build a standalone Python app for Windows, MacOS, and Linux, but not elegantly or simply.

Finally, Python is not the best choice when speed is an absolute priority in every aspect of the application. For that, you’re better off with C/C++ or another language of that caliber.</p>

							
